package com.example.kotlin_course_2.Part01

// math operation

fun main() {
    val num1 = 20
    val num2 = 10
    var result = num1 + num2
    println("num1 + num2 = $result")

    result = num1 - num2
    println("num1 - num2 = $result")

    result = num1 * num2
    println("num1 * num2 = $result")

    result = num1 / num2
    println("num1 / num2 = $result")

    result = num1 % num2
    println("num1 % num2 = $result")

    result += 2
    println("num1 += 2 = $result")

    result -= 2
    println("num1 -= 2 = $result")

    result *= 2
    println("num1 *= 2 = $result")

    result /= 2
    println("num1 /= 2 = $result")

    result %= 2
    println("num1 %= 2 = $result")


}