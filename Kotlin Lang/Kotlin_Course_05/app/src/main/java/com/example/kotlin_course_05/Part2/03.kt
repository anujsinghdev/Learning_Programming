package com.example.kotlin_course_05.Part2

fun main() {
    val myNumber = 2f
    val mySecondNumber = 3

    println("The sum of $myNumber and $mySecondNumber is ${myNumber + mySecondNumber}")

    val a = 5
    val b = 10

    println("a + b =  ${a + b}")
    println("a - b =  ${a - b}")
    println("a * b =  ${a * b}")
    println("a / b =  ${a / b}")
    println("a % b =  ${a % b}")

}