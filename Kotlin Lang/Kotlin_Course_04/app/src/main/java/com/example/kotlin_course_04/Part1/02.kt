package com.example.kotlin_course_04.Part1

fun main() {
    val byteVal: Byte = 127
    val shortVal: Short = 32767
    val intVal: Int = 2147483647
    val longVal: Long = 9223372036854775807


    val floatVal: Float = 3.4028235E38f
    val doubleVal: Double = 1.7976931348623157E308


    val charVal: Char = 'A'
    val stringVal: String = "Hello, Kotlin!"

    val booleanVal: Boolean = true

    println("Byte: $byteVal")
    println("Short: $shortVal")
    println("Int: $intVal")
    println("Long: $longVal")
    println("Float: $floatVal")
    println("Double: $doubleVal")
    println("Char: $charVal")
    println("String: $stringVal")
    println("Boolean: $booleanVal")




}